/*
	Sets up the vertex data by generating
	and initializing vertex data in Vertex
	Array and Vertex Buffer.
*/
void Skybox::SetupSkybox(void)
{
	glGenVertexArrays(1, &skyboxVAO);
	glGenBuffers(1, &skyboxVBO);
	glBindVertexArray(skyboxVAO);
	glBindBuffer(GL_ARRAY_BUFFER, skyboxVBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(GLfloat), (GLvoid*)0);
	glBindVertexArray(0);

	glDisableVertexAttribArray(0);

	log("Skybox setup complete.");
}

/*
	Loads a cubemap texture from 6 individual texture faces
	Order should be:
	+X (right)
	-X (left)
	+Y (top)
	-Y (bottom)
	+Z (front)
	-Z (back)

	faces	-	vector containing paths to all the six
	textures that are to be mapped on the cube.
*/
void Skybox::LoadCubemap(std::vector<const GLchar*> faces)
{
	glGenTextures(1, &cubemapTexture);

	int width, height;
	unsigned char* image;

	glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
	for (GLuint i = 0; i < faces.size(); i++)
	{
		image = SOIL_load_image(faces[i], &width, &height, 0, SOIL_LOAD_RGB);
		glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, image);
		SOIL_free_image_data(image);
	}
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
	glBindTexture(GL_TEXTURE_CUBE_MAP, 0);

	log("Skybox cubemap generated successfully.");
}

/*
	Binds the cubemap texture, passes the
	appropriate uniform to the shader program
	and renders the texture mapped cube.

	shader	-	Shader object used to render the skybox cube.
*/
void Skybox::Render(Shader shader)
{
	glDepthMask(GL_FALSE);// To turn depth writing off

	// Skybox cube
	glBindVertexArray(skyboxVAO);
	glActiveTexture(GL_TEXTURE0);
	glUniform1i(glGetUniformLocation(shader.program, "skybox"), 0);
	glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
	glDisableVertexAttribArray(0);
	glDepthMask(GL_TRUE);
}

